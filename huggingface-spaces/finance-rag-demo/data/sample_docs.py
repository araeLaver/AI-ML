def get_all_documents() -> List[Dict[str, Any]]:
    """모든 샘플 문서 반환"""
    return SAMPLE_DOCUMENTS.copy()


def get_documents_by_category(category: str) -> List[Dict[str, Any]]:
    """카테고리별 문서 필터링"""
    return [doc for doc in SAMPLE_DOCUMENTS if doc.get("category") == category]


def get_documents_by_company(company: str) -> List[Dict[str, Any]]:
    """기업별 문서 필터링"""
    return [doc for doc in SAMPLE_DOCUMENTS if doc.get("company") == company]


def get_categories() -> List[str]:
    """모든 카테고리 목록"""
    return list(set(doc.get("category", "") for doc in SAMPLE_DOCUMENTS))


def get_companies() -> List[str]:
    """모든 기업 목록"""
    return list(set(doc.get("company", "") for doc in SAMPLE_DOCUMENTS))


def get_document_count() -> int:
    """총 문서 수"""
    return len(SAMPLE_DOCUMENTS)
